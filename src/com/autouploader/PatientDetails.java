package com.autouploader;

public class PatientDetails {
	private int age;
	private String gender;
	public PatientDetails(int age, String gender) {
		super();
		this.age = age;
		this.gender = gender;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	

}
